#include "mlx_viewer.h"

int		get_nbr(char *str)
{
	int	n;
	int	nb;

	n = 0;
	nb = 0;
	while (str[n] >= '0' && str[n] <= '9')
	{
		nb = nb * 10 + str[n] - '0';
		n += 1;
	}
	return (nb);
}
