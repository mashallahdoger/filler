#include "filler.h"

int			ft_strcmp(char *s1, char *s2)
{
	int		n;

	n = 0;
	while (s1[n] && s2[n])
	{
		if (s2[n] - s1[n])
			return (s2[n] - s1[n]);
		n += 1;
	}
	return (0);
}
