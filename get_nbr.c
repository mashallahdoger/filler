#include "filler.h"

int			get_nbr(char *str)
{
	int		nb;
	int		n;

	nb = 0;
	n = 0;
	while (str[n] >= '0' && str[n] <= '9')
	{
		nb = nb * 10 + str[n] - '0';
		n++;
	}
	return (nb);
}
